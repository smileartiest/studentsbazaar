package com.studentsbazaar.studentsbazaarapp.model;

public class Tech_News_model {

    private String News_Id;
    private String News_Poster;
    private String News_Dis;
    private String Created_Date;


    // Getter Methods

    public String getNews_Id() {
        return News_Id;
    }

    public String getNews_Poster() {
        return News_Poster;
    }

    public String getNews_Dis() {
        return News_Dis;
    }

    public String getCreated_Date() {
        return Created_Date;
    }

    // Setter Methods

    public void setNews_Id(String News_Id) {
        this.News_Id = News_Id;
    }

    public void setNews_Poster(String News_Poster) {
        this.News_Poster = News_Poster;
    }

    public void setNews_Dis(String News_Dis) {
        this.News_Dis = News_Dis;
    }

    public void setCreated_Date(String Created_Date) {
        this.Created_Date = Created_Date;
    }

}
